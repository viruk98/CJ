package com.vk;

public class Switch_ClassA {

	
	public static void main(String[] args) {
		
		int a=10;
		switch (a) {
		default:System.out.println("default");
		case 40:System.out.println("10");
						
		case 20:System.out.println("20");
		
		case 30:System.out.println("30");
		

		}
	}
}
